nbr1 = int(input("Entrer le premier nombre "))
nbr2 = int(input("Entrer le deuxieme nombre "))
opera = input("Entre type d'operation que vous désirai éffectuer +,-,*,/ ")

if (opera == "+"):
    print(nbr1+nbr2)
elif  (opera == "-"):
    print(nbr1-nbr2)
elif (opera == "*"):
    print(nbr1*nbr2)
elif (opera == "/"):
    print(nbr1/nbr2)

